nbre1= int(input('entrer un nombre1:'))
symbole=str(input('entrer un symbole:'))
nbre2= int(input('entrer un nombre2:'))

if (symbole== "+"):
    resul=nbre1 + nbre2
    print (resul)
elif (symbole== "-"):
    resul=nbre1 - nbre1
    print (resul)
elif (symbole== "*"):
    resul=nbre1*nbre2
    print (resul)   
elif (symbole== "/"):
    resul=nbre1/nbre2
    print (resul)    
